# This is a band name generator based the users input

print("Hello, welcome to the band naming generator! A simple yet cool way to come up with an original band name.")
city = input("Which city did you grow up in?\n")
pet = input("What is your pets name?\n")
color = input("What is your favorite color?\n")
print("Your potential band name could be", city + " " + pet + " " + color)


